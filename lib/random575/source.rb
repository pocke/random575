  def self.source
    @source ||= begin
      s = __source
      result = {}
      s.each do |name, src|
        result[name] = {}.tap do |hash|
          src.format.each.with_index do |length, idx|
            src.poems.each do |poem|
              (hash[length] ||= []) << poem[idx]
            end
          end
        end
      end
      result
    end
  end

